package com.example.votingapp.adaptersNlists.AdminSide;

public class REVList {

    private final String name, position, id;

    public REVList(String name, String position, String id) {
        this.name = name;
        this.position = position;
        this.id = id;
    }

    public String getname() {

        return name;
    }

    public String getposition() {
        return position;
    }

    public String getid() {

        return id;
    }
}
